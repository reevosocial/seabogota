				<h2 class="fh5co-heading" >Organizers</span></h2>
						<p class="fh5co-lead animate-box" data-animate-effect="fadeInLeft">La Semana Internacional de las Educaciones Alternativas es un impulso organizado entre la Secretaría de Educación del distrito de Bogotá y diferentes organizaciones de la sociedad civil colombiana e internacional.</p>

						<p class="fh5co-lead animate-box" data-animate-effect="fadeInLeft">El Equipo Motor trabajó a escala distrital y local en la organización de la SEA. Desde Octubre de 2014 fomentó y propició encuentros de conversación e intercambio de saberes sobre educaciones alternativas a lo largo y ancho de la ciudad andina, contando con la participación de directores locales, rectores, educadores, investigadores, activistas de la comunidad, padres y madres de familia.</p>

